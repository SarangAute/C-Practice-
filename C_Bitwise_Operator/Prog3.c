#include<stdio.h>
int main(){
    int n = 2, r;
    r = ~n >> 2 << n + 3^7;
    /*PRIORITY: ~ + >> << ^
    ~n = 0000 0..0 0010 -> 1111 1..1 1101 = [-3]
    2+5 = 5 -> {-3 >> 2 << 5^7};
    ~n>>2= 1111 1..1 1111  =  -1   -> {-1 << 5^7}
    -1 << 5 = 1111 1..1 1110 0000  = -32
      -32^7 = 0000 0..0 0000 0111
          r = 1111 1..1 1110 0111 = -25
    */ 
    printf("result (~n >> 2 << n + 3^7)= %d \n", r); // r =-25
}